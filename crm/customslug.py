"""
In this module there is a custom slug generator for cyrillic symbols include

Attributes:
    alphabet (dict): dictionary with pairs of cyrillic symbols and latin transliteration for them
"""
from django.template.defaultfilters import slugify as django_slugify

alphabet = {'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
            'й': 'j', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
            'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch', 'ы': 'i', 'э': 'e', 'ю': 'yu',
            'я': 'ya'}


def slugify(s):
    """
    Overriding django slugify that allows to use russian words as well.

        Args:
            s (str): string to make slug from it
        Returns:
            (str) slug withot cyrillic symbols (transliterated)
    """
    return django_slugify(''.join(alphabet.get(w, w) for w in s.lower()))
